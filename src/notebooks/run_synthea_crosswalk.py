# Databricks notebook source
# MAGIC %md
# MAGIC # Red Bricks Insurance — Synthea Crosswalk (Phase 2)
# MAGIC
# MAGIC Maps Synthea-generated FHIR R4 patient bundles onto existing Red Bricks
# MAGIC MBR IDs, preserving all downstream joins. Writes rewritten bundles to
# MAGIC `clinical/fhir_bundles/` for downstream parsing by dbignite.
# MAGIC
# MAGIC **Input:** `{volume}/synthea_raw/fhir/` — Synthea FHIR bundles from Phase 1
# MAGIC **Output:** `{volume}/clinical/fhir_bundles/` — rewritten FHIR bundles with MBR IDs
# MAGIC
# MAGIC **Downstream:** `parse_fhir_with_dbignite.py` extracts Patient, Encounter,
# MAGIC Condition, and Observation Delta tables from the rewritten bundles.
# MAGIC
# MAGIC **Threading:** Bundle loading and FHIR file writes are parallelized with ThreadPoolExecutor.

# COMMAND ----------

dbutils.widgets.text("catalog", "main", "Catalog")
dbutils.widgets.text("schema", "red_bricks_insurance_dev", "Schema")

catalog = dbutils.widgets.get("catalog")
schema = dbutils.widgets.get("schema")
volume_base = f"/Volumes/{catalog}/{schema}/raw_sources"

print(f"Catalog: {catalog}, Schema: {schema}")
print(f"Volume base: {volume_base}")

# COMMAND ----------

# MAGIC %md
# MAGIC ## Step 1 — Load existing members and providers

# COMMAND ----------

from pyspark.sql import SparkSession

spark = SparkSession.builder.getOrCreate()

# Read members from Parquet (generated by run_data_generation.py)
df_members = spark.read.parquet(f"{volume_base}/members/")
members_data = [row.asDict() for row in df_members.collect()]
print(f"Members loaded: {len(members_data)} (MBR{members_data[0]['member_id'][-6:]}..MBR{members_data[-1]['member_id'][-6:]})")

# Read providers for NPI list
df_providers = spark.read.parquet(f"{volume_base}/providers/")
provider_npis = [row.npi for row in df_providers.select("npi").collect() if row.npi]
print(f"Providers loaded: {len(provider_npis)} NPIs")

# COMMAND ----------

# MAGIC %md
# MAGIC ## Step 2 — Run crosswalk
# MAGIC
# MAGIC This is the core operation:
# MAGIC 1. Load all Synthea FHIR bundles (parallel I/O)
# MAGIC 2. Build 1:1 demographic crosswalk (Synthea UUID → MBR ID)
# MAGIC 3. Rewrite every bundle with MBR IDs, names, addresses
# MAGIC 4. Extract flat encounters/labs/vitals matching bronze schema
# MAGIC 5. Write all outputs to the volume (parallel I/O)

# COMMAND ----------

