# MAGIC %md
# MAGIC ## Step 3 — Validate outputs
# MAGIC
# MAGIC Verifies FHIR bundles were rewritten with MBR IDs. Downstream parsing
# MAGIC (encounters, labs, vitals, conditions) is handled by `parse_fhir_with_dbignite.py`.

# COMMAND ----------

import json

# Check FHIR bundles
fhir_path = f"{volume_base}/clinical/fhir_bundles/"
fhir_files = [f for f in os.listdir(fhir_path) if f.endswith(".json")]
print(f"FHIR bundle files: {len(fhir_files)}")

# Sample FHIR bundle — verify MBR ID rewrite
if fhir_files:
    with open(os.path.join(fhir_path, fhir_files[0]), "r") as f:
        sample_bundle = json.load(f)
    for entry in sample_bundle.get("entry", []):
        r = entry.get("resource", {})
        if r.get("resourceType") == "Patient":
            print(f"\nSample FHIR Patient.id: {r.get('id')}")
            print(f"Sample FHIR Patient.name: {r.get('name')}")
            ident = r.get("identifier", [{}])[0]
            print(f"Sample FHIR identifier system: {ident.get('system')}")
            print(f"Sample FHIR identifier value: {ident.get('value')}")
            break

    # Count resource types in sample
    resource_types = {}
    for entry in sample_bundle.get("entry", []):
        rt = entry.get("resource", {}).get("resourceType", "Unknown")
        resource_types[rt] = resource_types.get(rt, 0) + 1
    print(f"\nResource types in sample bundle: {json.dumps(resource_types, indent=2)}")
    print(f"\nNext: Run parse_fhir_with_dbignite.py to extract clinical tables")

